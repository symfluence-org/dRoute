#!/usr/bin/env python3
"""
Gradient-correctness panel for the dRoute methods paper (GMD verification).

Verifies that dRoute's automatic-differentiation gradients of a calibration loss
w.r.t. per-reach Manning's n match centered finite differences, for every
differentiable router (Muskingum-Cunge via CoDiPack; Saint-Venant via the CVODES
adjoint). Reports max/median relative error and an AD-vs-FD scatter that should lie
on the 1:1 line.

Loss: L = mean_t (Q_outlet(t) - Q_obs(t))^2, with Q_obs generated by routing a
synthetic runoff through a known heterogeneous n field.

Usage:
    python experiments/gradient_verification.py --reaches 10 --n-time 300
"""

import argparse
import json
from pathlib import Path

import numpy as np

import droute
from idealized_chain import build_chain, synthetic_runoff, true_manning
from bow_at_banff_dds_vs_adam import set_manning

try:
    import matplotlib.pyplot as plt
    HAS_MPL = True
except ImportError:
    HAS_MPL = False


def make_router(method, net, dt, sv_nodes, grad):
    # NOTE: a router built with gradients enabled but not recording produces a broken
    # (near-zero) forward solution, so forward-only evaluations (loss, FD) must use the
    # gradients-OFF router; only the AD path enables gradients and records.
    if method == "sve":
        cfg = droute.SaintVenantConfig(); cfg.dt = dt
        for k, v in (("n_nodes", sv_nodes), ("initial_depth", 0.3),
                     ("initial_velocity", 0.1)):
            if hasattr(cfg, k):
                setattr(cfg, k, v)
        if hasattr(cfg, "enable_adjoint"):
            cfg.enable_adjoint = grad
        return droute.SaintVenantRouter(net, cfg)
    cfg = droute.RouterConfig(); cfg.dt = dt; cfg.enable_gradients = grad
    return droute.MuskingumCungeRouter(net, cfg)


def route_record(router, lateral, outlet, record):
    n_time, n_reach = lateral.shape
    if record:
        if hasattr(router, "reset_gradients"):
            router.reset_gradients()
        router.start_recording()
    sim = np.zeros(n_time)
    for t in range(n_time):
        for k in range(n_reach):
            router.set_lateral_inflow(k, float(lateral[t, k]))
        router.route_timestep()
        if record:
            router.record_output(outlet)
        sim[t] = router.get_discharge(outlet)
    if record:
        router.stop_recording()
    return sim


def loss_of(method, net, n_vals, lateral, obs, outlet, dt, sv_nodes):
    set_manning(net, n_vals)
    rt = make_router(method, net, dt, sv_nodes, grad=False)
    sim = route_record(rt, lateral, outlet, record=False)
    return float(np.mean((sim - obs) ** 2))


def ad_gradient(method, net, n_vals, lateral, obs, outlet, dt, sv_nodes):
    set_manning(net, n_vals)
    rt = make_router(method, net, dt, sv_nodes, grad=True)
    sim = route_record(rt, lateral, outlet, record=True)
    dL_dQ = (2.0 / len(sim)) * (sim - obs)
    rt.compute_gradients_timeseries(outlet, dL_dQ.tolist())
    grads = rt.get_gradients()
    n_reach = lateral.shape[1]
    return np.array([grads.get(f"reach_{i}_manning_n", 0.0) for i in range(n_reach)])


def fd_gradient(method, net, n_vals, lateral, obs, outlet, dt, sv_nodes, eps):
    n_reach = lateral.shape[1]
    g = np.zeros(n_reach)
    for i in range(n_reach):
        npls = n_vals.copy(); npls[i] += eps
        nmin = n_vals.copy(); nmin[i] -= eps
        lp = loss_of(method, net, npls, lateral, obs, outlet, dt, sv_nodes)
        lm = loss_of(method, net, nmin, lateral, obs, outlet, dt, sv_nodes)
        g[i] = (lp - lm) / (2 * eps)
    return g


def main():
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--reaches", type=int, default=10)
    ap.add_argument("--reach-len-km", type=float, default=15.0)
    ap.add_argument("--slope", type=float, default=0.001)
    ap.add_argument("--n-time", type=int, default=300)
    ap.add_argument("--dt", type=float, default=3600.0)
    ap.add_argument("--sv-nodes", type=int, default=6)
    ap.add_argument("--eps", type=float, default=1e-5)
    ap.add_argument("--methods", default="mc,sve")
    ap.add_argument("--seed", type=int, default=7)
    ap.add_argument("--out-dir", default="experiments/results_gradcheck")
    args = ap.parse_args()

    out = Path(args.out_dir); out.mkdir(parents=True, exist_ok=True)
    n = args.reaches; outlet = n - 1
    net = build_chain(n, args.reach_len_km * 1000.0, args.slope)
    lateral = synthetic_runoff(args.n_time, n, args.seed)
    n_true = true_manning(n, args.seed)
    n_eval = np.full(n, 0.035)  # evaluate gradients at the default n

    # Warm-up: the FIRST router built on a freshly-constructed network produces a broken
    # (near-zero) forward solution (router construction has a one-time side-effect on the
    # network); discard one throwaway route so obs/AD/FD are correct and consistent.
    _ = route_record(make_router("mc", net, args.dt, args.sv_nodes, grad=False), lateral, outlet, False)

    # synthetic observations from the true n field
    set_manning(net, n_true)
    obs = route_record(make_router("mc", net, args.dt, args.sv_nodes, grad=False), lateral, outlet, False)

    results = {}
    for method in [m.strip() for m in args.methods.split(",") if m.strip()]:
        label = {"mc": "Muskingum-Cunge", "sve": "Saint-Venant"}.get(method, method)
        print(f"\n=== {label} ===")
        try:
            ad = ad_gradient(method, net, n_eval, lateral, obs, outlet, args.dt, args.sv_nodes)
            fd = fd_gradient(method, net, n_eval, lateral, obs, outlet, args.dt, args.sv_nodes, args.eps)
        except Exception as e:
            print(f"  gradient check FAILED: {type(e).__name__}: {str(e)[:100]}")
            continue
        denom = np.maximum(np.abs(fd), 1e-12)
        rel = np.abs(ad - fd) / denom
        # cosine similarity of the gradient vectors (direction agreement)
        cos = float(np.dot(ad, fd) / (np.linalg.norm(ad) * np.linalg.norm(fd) + 1e-30))
        print(f"  max|AD|={np.abs(ad).max():.3e} max|FD|={np.abs(fd).max():.3e}")
        print(f"  rel.err: median={np.median(rel):.2e} max={rel.max():.2e}  cos={cos:.6f}")
        results[method] = {"label": label, "ad": ad.tolist(), "fd": fd.tolist(),
                           "rel_err_median": float(np.median(rel)),
                           "rel_err_max": float(rel.max()), "cosine": cos}

    (out / "gradcheck_summary.json").write_text(json.dumps(results, indent=2))
    print(f"\nWrote {out/'gradcheck_summary.json'}")

    if HAS_MPL and results:
        fig, ax = plt.subplots(figsize=(6.5, 6))
        allv = []
        for method, r in results.items():
            ad, fd = np.array(r["ad"]), np.array(r["fd"])
            allv += list(ad) + list(fd)
            ax.scatter(fd, ad, s=45, alpha=0.8,
                       label=f"{r['label']} (rel.err≤{r['rel_err_max']:.1e}, cos={r['cosine']:.4f})")
        lim = max(abs(min(allv)), abs(max(allv))) * 1.1
        ax.plot([-lim, lim], [-lim, lim], "k--", lw=1, alpha=0.6, label="1:1")
        ax.set_xlim(-lim, lim); ax.set_ylim(-lim, lim)
        ax.set_xlabel("finite-difference dL/dn"); ax.set_ylabel("automatic-differentiation dL/dn")
        ax.set_title("dRoute gradient verification: AD vs finite difference")
        ax.legend(loc="upper left", fontsize=8); ax.grid(alpha=0.3); ax.set_aspect("equal")
        fig.savefig(out / "gradcheck_scatter.png", dpi=150, bbox_inches="tight")
        plt.close(fig)
        print(f"Wrote {out/'gradcheck_scatter.png'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
